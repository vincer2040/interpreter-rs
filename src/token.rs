#[derive(PartialEq, Eq, Debug)]
pub enum Token {
    Illegal,
    Eof,
    Ident(std::sync::Arc<str>),
    Int(std::sync::Arc<str>),
    Assign,
    Plus,
    Minus,
    Slash,
    Asterisk,
    Bang,
    Lt,
    Gt,
    Eq,
    NotEq,
    Comma,
    Semicolon,
    LParen,
    RParen,
    LSquirly,
    RSquirly,
    Function,
    Let,
    If,
    Else,
    Return,
    True,
    False,
}
